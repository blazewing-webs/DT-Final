export default function SeedPage() {
    const [status, setStatus] = useState<"idle" | "seeding" | "success" | "error">("idle");
    const [log, setLog] = useState<string[]>([]);

    const seedDatabase = async () => {
        setStatus("seeding");
        setLog([]);

        try {
            // Seed Articles
            setLog(prev => [...prev, "--- Seeding Articles ---"]);
            for (const article of SAMPLE_ARTICLES) {
                setLog(prev => [...prev, `Creating Article: ${article.title}...`]);
                const slug = generateSlug(article.title);
                await addDoc(collection(db, "articles"), {
                    title: article.title,
                    slug: slug,
                    category: article.category,
                    author: article.author,
                    image: article.imageUrl,
                    priority: article.priority,
                    excerpt: article.excerpt,
                    content: article.content,
                    date: Timestamp.now(),
                    createdAt: Timestamp.now()
                });
                setLog(prev => [...prev, `тЬЕ Created Article: ${article.title}`]);
            }

            // Seed Magazines
            setLog(prev => [...prev, "--- Seeding Magazines ---"]);
            for (const mag of SAMPLE_MAGAZINES) {
                setLog(prev => [...prev, `Creating Magazine: ${mag.title}...`]);
                await addDoc(collection(db, "magazines"), {
                    title: mag.title,
                    month: mag.month,
                    coverUrl: mag.coverUrl,
                    pdfUrl: mag.pdfUrl,
                    createdAt: Timestamp.now()
                });
                setLog(prev => [...prev, `тЬЕ Created Magazine: ${mag.title}`]);
            }

            setStatus("success");
            setLog(prev => [...prev, "ЁЯОЙ All content seeded successfully!"]);
        } catch (error) {
            console.error(error);
            setStatus("error");
            setLog(prev => [...prev, `тЭМ Error: ${error}`]);
        }
    };

    return (
        <div className="min-h-screen p-8 bg-neutral-50 font-sans">
            <div className="max-w-2xl mx-auto bg-white p-8 rounded-xl shadow">
                <h1 className="text-2xl font-bold mb-6">Database Seeder</h1>

                <div className="mb-8 p-4 bg-yellow-50 text-yellow-800 rounded-lg border border-yellow-200 flex items-start gap-3">
                    <AlertCircle className="w-5 h-5 mt-0.5" />
                    <div>
                        <p className="text-sm font-bold mb-1">Make sure you have placed the magazine covers in public/magazines/ folder!</p>
                        <p className="text-sm">
                            - magazine-1.jpg<br />
                            - magazine-2.jpg<br />
                            - magazine-3.jpg
                        </p>
                    </div>
                </div>

                <div className="flex gap-4 mb-8">
                    <button
                        onClick={seedDatabase}
                        disabled={status === "seeding" || status === "success"}
                        className="px-6 py-3 bg-dravida-red text-white font-bold rounded-lg hover:bg-black transition-colors disabled:opacity-50 flex items-center gap-2"
                    >
                        {status === "seeding" && <Loader2 className="w-4 h-4 animate-spin" />}
                        {status === "success" && <CheckCircle className="w-4 h-4" />}
                        {status === "idle" ? "Start Seeding All" : status === "seeding" ? "Seeding..." : "Seeding Complete"}
                    </button>

                    {status === "success" && (
                        <div className="flex gap-2">
                            <a href="/" className="px-4 py-3 bg-neutral-900 text-white rounded-lg hover:bg-neutral-800">
                                Go to Home
                            </a>
                        </div>
                    )}
                </div>

                <div className="bg-neutral-900 text-green-400 p-4 rounded-lg font-mono text-sm h-64 overflow-y-auto">
                    {log.length === 0 ? (
                        <span className="text-neutral-500">// Logs will appear here...</span>
                    ) : (
                        log.map((line, i) => <div key={i}>{line}</div>)
                    )}
                </div>
            </div>
        </div>
    );
}
